#include<iostream>

using namespace std;

int main(){
	
	int a=8 ,b=4;
	
	cout<<"Operators in c++"<<endl;
	
	cout<<" following are the Operater in c++"<<endl;
	
	//Arithmetic Operators
	
	cout<<" the value of a+b is :"<<a+b<<endl;
	
	
	cout<<" the value of a-b is :"<<a-b<<endl;
	
	
	cout<<" the value of a*b is :"<<a*b<<endl;
	
	cout<<"the value of a/b is:"<<a/b<<endl;
	
	cout<<" the value of a%b is :"<<a%b<<endl;


	
	
	cout<<" the value of a++ is :"<<a++<<endl;
	
	
	
	cout<<" the value of a-- is :"<<a--<<endl;
	
	
	
	cout<<" the value of ++a is :"<<++a<<endl;
	
	
	
	cout<<" the value of --a is :"<<--a<<endl;
	
	system ("pause");
	
	return 0;
}
